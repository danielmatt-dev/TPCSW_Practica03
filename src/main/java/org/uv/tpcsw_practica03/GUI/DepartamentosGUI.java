    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        Departamento depto = daoDepto.findById(Long.valueOf(txtClave.getText()));
        
        txtClave.setText(String.valueOf(depto.getClave()));
        txtNombre.setText(depto.getNombre());
        
        model.addColumn("Clave");
        model.addColumn("Nombre");
        
        model.addRow(
                new Object[]{
                    depto.getClave(), 
                    depto.getNombre(), 
                });
        
        jtDeptos.setModel(model);
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void jtDeptosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtDeptosMouseClicked
        
        int fila = jtDeptos.getSelectedRow();
        
        long clave = (long) jtDeptos.getValueAt(fila, 0);
        String nombre = (String) jtDeptos.getValueAt(fila, 1);
       
        txtClave.setText(String.valueOf(clave));
        txtNombre.setText(nombre);
       
        
        
    }//GEN-LAST:event_jtDeptosMouseClicked

