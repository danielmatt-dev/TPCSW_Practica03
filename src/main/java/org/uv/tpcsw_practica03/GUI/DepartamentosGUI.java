    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Departamento depto = new Departamento();
        depto.setClave(Long.valueOf(txtClave.getText()));
        depto.setNombre(txtNombre.getText());
        if(daoDepto.save(depto)) {
            JOptionPane.showMessageDialog(null, "Departamento guardado con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al guardar departamento");
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        Long clave = Long.valueOf(txtClave.getText());
        
        Departamento depto = new Departamento();
        depto.setClave(clave);
        depto.setNombre(txtNombre.getText());
        
        if(daoDepto.update(depto, clave)) {
            JOptionPane.showMessageDialog(null, "Departamento actualizado con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al actualizar departamento");
        }
        
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        Long id = Long.valueOf(txtClave.getText());
        
        if(daoDepto.delete(id)) {
            JOptionPane.showMessageDialog(null, "Departamento eliminado con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "Error al elminar departamento");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTodoActionPerformed

        List<Departamento> deptos = daoDepto.findAll();
        
        model.addColumn("Clave");
        model.addColumn("Nombre");
        
        if(deptos != null){ 
            for (Departamento depto : deptos) {
                model.addRow(
                        new Object[]{
                            depto.getClave(), 
                            depto.getNombre(), 
                        });
            }
        }

        jtDeptos.setModel(model);
    }//GEN-LAST:event_btnBuscarTodoActionPerformed

